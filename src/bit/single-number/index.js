exports.singleNumber = (nums) => {
  let ret = 0;
  nums.forEach((n) => {
    ret ^= n;
  });
  return ret;
};

/**
 * [0b10,0b10,0b01]
 *
 * [0]
 * 0b00
 * ^
 * 0b10
 * =
 * 0b10
 *
 * [1]
 *
 * 0b10
 * ^
 * 0b10
 * =
 * 0b00
 *
 * [2]
 * 0b00
 * ^
 * 0b01
 * =
 * 0b01
 *
 * & 与 都1时为1
 * | 或 都为0时为0
 * ^ 异或 相同为0, 相异为1
 * ~ 取反 1为0, 0为1
 *
 * 0^2=2 2^2=0
 * 任何一个数跟自身 异或 都为0
 */
